"use strict";
// 36. T-Shirt: Write a function called make_shirt() that accepts a size and the text 
// of a message that should be printed on the shirt. The function should print a 
// sentence summarizing the size of the shirt and the message printed on it. Call the function.
function make_shirt(size, text) {
    console.log(`You order a ${size}, shirt from gucci. The text written on it is${text}.`);
}
make_shirt(`Medium`, ` Always be Happy.`);
make_shirt("Small", " Be the the brave.");
